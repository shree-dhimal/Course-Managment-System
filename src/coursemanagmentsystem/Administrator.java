    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        
        ArrayList al = new ArrayList();
        String instructor_name = jTextField3.getText();
	String s = instructor_name.toUpperCase();
        if (jTextField3.getText().equals("")){
         JOptionPane.showMessageDialog(null,"Please Enter the Instructor name to remove!!"+"\n","ERROR",JOptionPane.ERROR_MESSAGE);
      }else{
        try {
            FileReader f = new FileReader("Instructor_course.txt");
            Scanner sc = new Scanner(f);
            while((sc.hasNext())) {
                al.add(sc.nextLine());
                }
            FileWriter fw = new FileWriter("Instructor_course.txt");
            Iterator itr = al.iterator();
            while (itr.hasNext()){
                String ss = (String)itr.next();
                String[] s2 = ss.split("\t");
            }
            for(int i = 0;i<al.size();i++){
                String s1 = (String) al.get(i);
                String[] s2 = s1.split("\t");
                if(!s.equals(s2[0])){
                fw.write(s1+"\n");
                }
            } 
            fw.close();
            
            
            JOptionPane.showMessageDialog(this,"sucessfully removed the instructor"+" "+s); 
            jTextField3.setText("");
            
        }catch(IOException ex){
            JOptionPane.showMessageDialog(this,"There is no any file of Instructor and their assigned course","ERROR",JOptionPane.ERROR_MESSAGE);
        }
        }
        
    }//GEN-LAST:event_jButton6ActionPerformed

