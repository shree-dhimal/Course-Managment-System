    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        ArrayList a = new ArrayList();
        String sname = jTextField2.getText();
        String b = sname.toUpperCase();
        String smodule = jTextField3.getText();
        String c = smodule.toUpperCase();
        
        if (jTextField2.getText().equals("") || jTextField3.getText().equals("") || jTextField4.getText().equals("") ){
         
            JOptionPane.showMessageDialog(null,"Donot leave any blank space!!"+"\n","ERROR",JOptionPane.ERROR_MESSAGE);
            }else{
            String marks_obtained = jTextField4.getText();
        float f= Float.parseFloat(marks_obtained);
        a.add(c+"\t"+b+"\t"+marks_obtained);
            try {
            FileWriter marks = new FileWriter("Module_student_marks.txt",true);
            Iterator itr1 = a.iterator();
            while (itr1.hasNext()){
               marks.write(itr1.next() + "\n");
            }
            marks.close();
            JOptionPane.showMessageDialog(this,"Sucessfully Updated:");
      
        }catch (IOException e) {
        JOptionPane.showMessageDialog(this,"An error occured","ERROR",JOptionPane.ERROR_MESSAGE);
        e.printStackTrace();
        }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

