    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        //Instantiating the File class
        String old = jTextField2.getText();
        String oldString = old.toUpperCase();
        String New = jTextField1.getText();
        String newString = New.toUpperCase();
       if (jTextField2.getText().equals("") || (jTextField1.getText().equals(""))){
         JOptionPane.showMessageDialog(null,"Please enter the values!!"+"\n","ERROR",JOptionPane.ERROR_MESSAGE);
      }else{
        File fileToBeModified = new File("Courses.txt");
         
        String oldContent = "";
         
        BufferedReader reader = null;
         
        FileWriter writer = null;
         
        try
        {
            reader = new BufferedReader(new FileReader(fileToBeModified));
             
            //Reading all the lines of input text file into oldContent
             
            String line = reader.readLine();
             
            while (line != null) 
            {
                oldContent = oldContent + line + System.lineSeparator();
                 
                line = reader.readLine();
            }
             
            //Replacing oldString with newString in the oldContent
             
            String newContent = oldContent.replaceAll(oldString, newString);
             
            //Rewriting the input text file with newContent
             
            writer = new FileWriter(fileToBeModified);
             
            writer.write(newContent);
        }
        catch (IOException e)
        {
            e.printStackTrace();
        }
        finally
        {
            try
            {
                //Closing the resources
                 
                reader.close();
                 
                writer.close();
                JOptionPane.showMessageDialog(this,"Sucessfully Changed course:"+oldString+"\n"+"into"+newString+"");
                jTextField2.setText("");
                jTextField1.setText("");
            } 
            catch (IOException e) 
            {
                e.printStackTrace();
            }
        }
       }
    }//GEN-LAST:event_jButton2ActionPerformed

