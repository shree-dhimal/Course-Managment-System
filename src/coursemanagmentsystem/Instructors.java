    private void jCheckBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCheckBox1ActionPerformed
        // TODO add your handling code here:
        try {
            FileReader fr = new FileReader("Student_module.txt");
            Scanner fs = new Scanner(fr);
            while(fs.hasNext()) {
		//System.out.println(fs.nextLine() + "\t");
                if(jCheckBox1.isSelected()){
                jTextArea1.setText(fs.nextLine());
                    
                }else{
                    jTextArea1.setText(fs.nextLine());
                    
                    
            }
        }
            } catch (FileNotFoundException e) {
            JOptionPane.showMessageDialog(this,"Student details file not found!!","ERROR",JOptionPane.ERROR_MESSAGE);
	      e.printStackTrace();;
                        
            }
    }//GEN-LAST:event_jCheckBox1ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String Instructor_name = "";
        String Course_name = "";
        String a = null;
        ArrayList al = new ArrayList();
        String instructor = jTextField1.getText();
	String word = instructor.toUpperCase();
        if (jTextField1.getText().equals("")){
         JOptionPane.showMessageDialog(null,"Please Enter the instructor name to find the Assigned course!!"+"\n","ERROR",JOptionPane.ERROR_MESSAGE);
      }else{
        try {
            File f = new File("Instructor_course.txt");
            BufferedReader freader = new BufferedReader(new FileReader(f));
            String s;
            while((s = freader.readLine()) != null) {
                String[] st = s.split("\t");
                Instructor_name = st[0];
                Course_name = st[1];
                
                
            if(Instructor_name.equals(word)){
                
                al.add(Course_name+" \t ");
            
            }
                
                }
            JOptionPane.showMessageDialog(this, word+" "+ " is assigned to Courses:"+"\n"+al);  
            jTextField1.setText("");
        }catch(IOException ex){
            JOptionPane.showMessageDialog(this,"There is no any file of Instructor and their assigned courses","ERROR",JOptionPane.ERROR_MESSAGE);
        }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

